/**
 * Enum for all the possible mouse buttons.
 */
module mouseButtons {
    /**
     * The left mouse button.
     * @type {number}
     */
    export var Left = 0;

    /**
     * The middle button or scroll wheel.
     * @type {number}
     */
    export var Middle = 1;

    /**
     * The right button.
     * @type {number}
     */
    export var Right = 2;
}
export = mouseButtons;
