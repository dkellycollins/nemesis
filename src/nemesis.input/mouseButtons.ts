/**
 * Enum for all the possible mouse buttons.
 */
module mouseButtons {
    /**
     * The left mouse button.
     * @type {number}
     */
    export var LEFT = 0;

    /**
     * The middle button or scroll wheel.
     * @type {number}
     */
    export var MIDDLE = 1;

    /**
     * The right button.
     * @type {number}
     */
    export var RIGHT = 2;
}
export = mouseButtons;
